<script>
   document.addEventListener('DOMContentLoaded', function() {
      // Initialize tooltips
      var tooltipTriggerList = [].slice.call(document.querySelectorAll('[data-bs-toggle="tooltip"]'))
      var tooltipList = tooltipTriggerList.map(function (tooltipTriggerEl) {
         return new bootstrap.Tooltip(tooltipTriggerEl)
      });
      
      // Highlight differences in comparison
      const comparisonTable = document.querySelectorAll('.comparison-table');
      comparisonTable.forEach(table => {
         const rows = table.querySelectorAll('tbody tr');
         
         rows.forEach(row => {
            const cells = row.querySelectorAll('td');
            let values = [];
            
            cells.forEach(cell => values.push(cell.textContent.trim()));
            
            const allSame = values.every(v => v === values[0]);
            
            if (!allSame) {
               cells.forEach(cell => {
                  if (row.querySelector('th').textContent.includes('Barrel Type') || 
                      row.querySelector('th').textContent.includes('Taste Profile') ||
                      row.querySelector('th').textContent.includes('Packaging')) {
                     // Add visual indication for key differences
                     const text = cell.textContent;
                     if (text.includes('Bourbon')) {
                        cell.innerHTML = text.replace('Bourbon', '<span class="badge bg-info">Bourbon</span>');
                     }
                     if (text.includes('Premium')) {
                        cell.innerHTML = text.replace('Premium', '<span class="badge bg-success">Premium</span>');
                     }
                     if (text.includes('Signature')) {
                        cell.innerHTML = text.replace('Signature', '<span class="badge bg-warning">Signature</span>');
                     }
                  }
               });
            }
         });
      });
   });
</script>
